// Global Scope — JavaScript (Very Easy 😊)
// Global scope means a variable is created outside all functions, so it can be used anywhere in the program.

// 🔹 Simple Example
// let a = 10; // global variable

// function show() {
//   console.log(a);
// }

// show(); // 10
// console.log(a); // 10
// 👉 a can be used inside and outside the function.

// 🔹 Another Example
// let name = "Ali";

// function greet() {
//   console.log("Hello " + name);
// }

// greet();
// Output:

// Hello Ali
// 👉 name is global, so the function can access it.

// 🔑 Easy Rule
// Global variables are accessible everywhere in the code.

// ⚠️ Important
// Too many global variables can cause conflicts and bugs, so use them carefully.