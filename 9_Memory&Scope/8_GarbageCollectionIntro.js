// Garbage Collection (Intro) — JavaScript (Very Easy 😊)
// Garbage Collection is the process where JavaScript automatically removes unused memory.

// 👉 It cleans up variables and objects that are no longer needed.

// 🔹 Simple Example
// function test() {
//   let x = 10;
// }

// test();
// After the function finishes, x is no longer used, so JavaScript removes it from memory automatically.

// 🔹 Why Garbage Collection is Important
// ✔ Frees unused memory
// ✔ Improves performance
// ✔ Prevents memory leaks

// 🔹 Easy Way to Think
// Imagine memory like a room 🧹
// When items are no longer needed, JavaScript cleans the room automatically.

// 🔑 Easy Rule
// Unused variables → automatically removed from memory.

