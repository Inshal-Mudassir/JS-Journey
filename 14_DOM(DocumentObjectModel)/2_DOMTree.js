// DOM Tree — JavaScript (Very Easy 😊)
// The DOM Tree is a tree structure of an HTML page.

// 👉 Every HTML element becomes a node (object) in the tree.

// 🔹 Example HTML
// <html>
//   <body>
//     <h1>Hello</h1>
//     <p>Welcome</p>
//   </body>
// </html>
// 🔹 DOM Tree Structure
// Document
//  └── html
//       └── body
//            ├── h1
//            │    └── "Hello"
//            └── p
//                 └── "Welcome"
// 🔹 Key Idea
// document → root (top)

// elements → nodes

// text → child nodes

// 🔑 Easy Rule
// DOM Tree = hierarchical structure of HTML elements that JavaScript can access. 🌳