// Attributes — JavaScript (Very Easy 😊)
// Attributes are extra information added to HTML elements (like id, class, src, etc.).

// 🔹 Example HTML
// <img src="image.jpg" id="pic" />
// 👉 src and id are attributes

// 🔹 Get Attribute
// let img = document.querySelector("img");

// console.log(img.getAttribute("src"));
// 👉 Gets value of src

// 🔹 Set Attribute
// img.setAttribute("src", "new.jpg");
// 👉 Changes attribute value

// 🔹 Remove Attribute
// img.removeAttribute("id");
// 🔑 Easy Rule
// Attributes = extra details of HTML elements that JavaScript can get, set, or remove. 😊