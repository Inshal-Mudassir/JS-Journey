// DOM — JavaScript (Very Easy 😊)
// DOM stands for Document Object Model.

// It is a programming interface that represents an HTML webpage as objects so JavaScript can read and change the page.

// 🔹 Simple Idea
// HTML page → converted into a tree of objects.

// Example HTML:

// <h1>Hello</h1>
// <p>Welcome</p>
// DOM representation:

// Document
//  ├── h1
//  │    └── "Hello"
//  └── p
//       └── "Welcome"
// 🔹 Example with JavaScript
// document.querySelector("h1").textContent = "Hi";
// 👉 JavaScript changes the webpage content using the DOM.

// 🔑 Easy Rule
// DOM = a structure of the webpage that JavaScript can access and modify. 🌐