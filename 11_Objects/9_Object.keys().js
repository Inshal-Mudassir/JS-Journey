// Object.keys() — JavaScript 😊
// Object.keys() is used to get all the keys (property names) of an object.

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20,
//   city: "Lahore"
// };

// let keys = Object.keys(person);

// console.log(keys);
// Output

// ["name", "age", "city"]
// 👉 It returns an array of object keys.

// 🔑 Easy Rule
// Object.keys(object) → returns all property names of the object as an array.