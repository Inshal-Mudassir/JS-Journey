// Object Destructuring — JavaScript (Very Easy 😊)
// Object destructuring lets you extract values from an object and store them in variables easily.

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20,
//   city: "Lahore"
// };

// let { name, age } = person;

// console.log(name);
// console.log(age);
// Output

// Ali
// 20
// 👉 name gets person.name
// 👉 age gets person.age

// 🔹 Rename Variables
// let { name: userName } = person;

// console.log(userName);
// 👉 userName will store the value of name.

// 🔑 Easy Rule
// Object Destructuring = quickly take values from an object into variables. 😊