// Object.values() — JavaScript 😊
// Object.values() is used to get all the values of an object.

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20,
//   city: "Lahore"
// };

// let values = Object.values(person);

// console.log(values);
// Output

// ["Ali", 20, "Lahore"]
// 👉 It returns an array of object values.

// 🔑 Easy Rule
// Object.values(object) → returns all values of the object as an array.