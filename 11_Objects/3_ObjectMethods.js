// Object Methods — JavaScript (Very Easy 😊)
// Object methods are functions inside an object.

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20,
//   greet: function() {
//     console.log("Hello!");
//   }
// };

// person.greet();
// Output

// Hello!
// 👉 greet() is an object method.

