// Dot vs Bracket Notation — JavaScript 😊
// Both are used to access object properties.

// 🔹 Dot Notation
// let person = {
//   name: "Ali",
//   age: 20
// };

// console.log(person.name);
// Output

// Ali
// 👉 Used when the property name is known.

// 🔹 Bracket Notation
// let person = {
//   name: "Ali",
//   age: 20
// };

// console.log(person["age"]);
// Output

// 20
// 👉 Used when the property name is in a variable or dynamic.

// 🔑 Easy Rule
// object.name → Dot notation

// object["name"] → Bracket notation