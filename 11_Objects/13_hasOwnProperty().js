// hasOwnProperty() — JavaScript 😊
// hasOwnProperty() checks whether an object contains a specific property.

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20
// };

// console.log(person.hasOwnProperty("name"));
// Output

// true
// 👉 Because name exists in the object.

// 🔹 Another Example
// console.log(person.hasOwnProperty("city"));
// Output

// false
// 👉 Because city is not in the object.

// 🔑 Easy Rule
// object.hasOwnProperty("key") → checks if that property exists in the object.