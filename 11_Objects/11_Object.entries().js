// Object.entries() — JavaScript 😊
// Object.entries() is used to get both keys and values of an object as pairs.

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20,
//   city: "Lahore"
// };

// let entries = Object.entries(person);

// console.log(entries);
// Output

// [
//   ["name", "Ali"],
//   ["age", 20],
//   ["city", "Lahore"]
// ]
// 👉 Each item is an array with [key, value].

// 🔑 Easy Rule
// Object.entries(object) → returns key‑value pairs as an array. 😊