// Object Properties — JavaScript (Very Easy 😊)
// Object properties are the data stored inside an object (key : value).

// 🔹 Example
// let person = {
//   name: "Ali",
//   age: 20,
//   city: "Lahore"
// };
// Here:

// Property	Value
// name	"Ali"
// age	20
// city	"Lahore"
// 👉 name, age, city are object properties.

// 🔹 Access Properties
// 1️⃣ Dot Notation

// console.log(person.name);
// Output

// Ali
// 2️⃣ Bracket Notation

// console.log(person["age"]);
// Output

// 20
// 🔑 Easy Rule
// Object Property = key + value stored inside an object. 😊