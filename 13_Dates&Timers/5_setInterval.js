// setInterval() — JavaScript (Very Easy 😊)
// setInterval() is used to run a function repeatedly after a fixed time interval.

// 🔹 Syntax
// setInterval(function, time);
// 👉 time is in milliseconds
// 1000 ms = 1 second

// 🔹 Example
// setInterval(function() {
//   console.log("Hello");
// }, 2000);
// Output every 2 seconds

