// setTimeout() — JavaScript (Very Easy 😊)
// setTimeout() is used to run a function after a certain time delay.

// 🔹 Syntax
// setTimeout(function, time);
// 👉 time is in milliseconds
// 1000 ms = 1 second

// 🔹 Example
// setTimeout(function() {
//   console.log("Hello");
// }, 2000);
// Output after 2 seconds

// Hello
// 🔹 Using Arrow Function
// setTimeout(() => {
//   console.log("Hi");
// }, 1000);
// 🔑 Easy Rule
// setTimeout() → runs code after a delay. ⏳