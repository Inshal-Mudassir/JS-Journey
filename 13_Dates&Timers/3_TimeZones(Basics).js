// Time Zones (Basics) — JavaScript 😊
// A time zone shows the local time of a specific region of the world.

// JavaScript can handle time zones using the Date object.

// 🔹 Get Local Time
// let now = new Date();

// console.log(now.toString());
// 👉 Shows your system’s local date and time.

// 🔹 Get UTC Time
// let now = new Date();

// console.log(now.toUTCString());
// 👉 Shows time in UTC (Universal Time Coordinated).

// 🔹 Get Time Zone Offset
// let now = new Date();

// console.log(now.getTimezoneOffset());
// 👉 Returns difference (in minutes) between local time and UTC.

// 🔑 Easy Rule
// Time Zone = difference between local time and UTC. 🌍