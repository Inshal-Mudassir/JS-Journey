// Escape Characters — JavaScript (Very Easy 😊)
// Escape characters are used to add special characters inside strings.

// They start with a backslash \.

// 🔹 \" → Double Quote
// let text = "He said \"Hello\"";

// console.log(text);
// Output

// He said "Hello"
// 🔹 \' → Single Quote
// let text = 'It\'s JavaScript';
// Output

// It's JavaScript
// 🔹 \n → New Line
// let text = "Hello\nWorld";

// console.log(text);
// Output

// Hello
// World
// 🔹 \t → Tab Space
// let text = "Hello\tWorld";
// 🔑 Easy Rule
// Escape character (\) lets you include special characters inside strings. 😊