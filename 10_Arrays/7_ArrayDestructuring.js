// Array Destructuring — JavaScript (Very Easy 😊)
// Array destructuring means taking values from an array and storing them in variables easily.

// 🔹 Basic Example
// let fruits = ["Apple", "Banana", "Mango"];

// let [a, b, c] = fruits;

// console.log(a); // Apple
// console.log(b); // Banana
// console.log(c); // Mango
// 👉 Values are assigned in order.

// 🔹 Skipping Values
// let numbers = [10, 20, 30];

// let [a, , c] = numbers;

// console.log(a); // 10
// console.log(c); // 30
// 👉 , skips the middle value.

// 🔹 Default Values
// let numbers = [5];

// let [a, b = 10] = numbers;

// console.log(a); // 5
// console.log(b); // 10
// 👉 b gets default value.

// 🔑 Easy Rule
// Array destructuring = quickly extract array values into variables.