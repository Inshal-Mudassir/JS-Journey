// Spread Operator with Arrays — JavaScript (Very Easy 😊)
// The Spread Operator (...) is used to expand elements of an array.

// 🔹 Copy an Array
// let arr1 = [1, 2, 3];
// let arr2 = [...arr1];

// console.log(arr2);
// Output

// [1, 2, 3]
// 👉 Creates a copy of the array.

// 🔹 Merge Arrays
// let a = [1, 2];
// let b = [3, 4];

// let result = [...a, ...b];

// console.log(result);
// Output

// [1, 2, 3, 4]
// 👉 Combines arrays into one.

// 🔹 Add Elements
// let numbers = [2, 3];

// let newNumbers = [1, ...numbers, 4];

// console.log(newNumbers);
// Output

// [1, 2, 3, 4]
// 🔑 Easy Rule
// ...array spreads all elements of the array.