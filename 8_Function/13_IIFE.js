// IIFE — Immediately Invoked Function Expression (Very Easy 😊)
// An IIFE is a function that runs immediately after it is created.

// 🔹 Basic Syntax
// (function () {
//   console.log("Hello!");
// })();
// 👉 The function runs instantly.

// 🔹 With Arrow Function
// (() => {
//   console.log("Hi!");
// })();
// 🔹 Why Use IIFE?
// ✔ Run code only once
// ✔ Create private variables
// ✔ Avoid polluting global scope

// 🔑 Easy Rule to Remember
// ( function )();
// 👉 Wrap function in ()
// 👉 Add () at the end to run it immediately

