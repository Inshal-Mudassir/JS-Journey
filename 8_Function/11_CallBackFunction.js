// Callback Functions — JavaScript (Very Easy 😊)
// A callback function is a function that is passed into another function as an argument, and runs later.

// 🔹 Simple Example
// function greet(name) {
//   console.log("Hello " + name);
// }

// function processUser(callback) {
//   callback("Ali");
// }

// processUser(greet);
// 👉 greet is the callback function.

// 🔹 Even Shorter Example
// function sayHi() {
//   console.log("Hi!");
// }

// setTimeout(sayHi, 2000);
// 👉 sayHi runs after 2 seconds.
// It is used as a callback.

// 🔑 Easy Rule to Remember
// Callback = a function given to another function to run later